package server;

import java.util.Random;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.io.ClassPathResource;
import org.springframework.core.io.Resource;
import org.springframework.data.repository.init.Jackson2RepositoryPopulatorFactoryBean;

@Configuration
public class Config {

    /**
     * A Bean for the java.util.Random import
     * @return a Random Object
     */
    @Bean
    public Random getRandom() {
        return new Random();
    }

    /**
     * The configuration for the bean that populates the Activities Repository with
     * the information from activities.json
     * @return a Jackson Populator bean
     */
    @Bean
    public Jackson2RepositoryPopulatorFactoryBean getRespositoryPopulator() {
        Jackson2RepositoryPopulatorFactoryBean factory = new Jackson2RepositoryPopulatorFactoryBean();
        factory.setResources(new Resource[]{new ClassPathResource("activities.json")});
        return factory;
    }
}